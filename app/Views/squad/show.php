<div id="info">W 1998 roku dwóch Mc’s: Nullo i Szad postanowili wspólnie nagrać kilka traków i tak rok później pod nazwą „Poetikanonim” ukazał się pierwszy w historii Wałbrzycha nielegal „…Od zawsze tu byłem”.<br />
    W 2000-2001 roku ujrzał światło dzienne solowy projekt Nulla pt. „Wszystko rymowanym słowem”, który spotkał się z dużym lokalnym rozgłosem, a także z pozytywnymi opiniami. Tym materiałem Nullo wygrał festiwal hip hopowy w Żarowie, gdzie udział brało kilkanaście zespołów z całej Polski. Pierwszą nagrodą w tym konkursie była nagrywka i pomoc Magiery. Na płycie Nulla debiutował Dj Sph, który wkrótce potem zaczął współpracować z czołówką polskiej sceny hip-hopowej. W tym samym czasie Pork nagrywał w squadzie W-klika (obecnie Tora Tora Squad). <br />
    W sierpniu 2001 roku Pork i Nullo nagrali wspólny numer „Status Quo” do którego dograł się Szad, a skreczami zajął się Dj Sph. W tym czteroosobowym składzie powstawały kolejne produkcje jeszcze pod nazwą Peo-ete-ik, z których część jako demo została rozesłana po wytwórniach płytowych. Dosyć szybko nastąpił odzew ze strony kilku wytwórni. Skład z Dolnego Śląska wybrał ofertę Cameya, która była najbardziej konkretna. Materiał na premierowy krążek już pod nazwą Trzeci Wymiar gromadzony był od połowy 2002 roku. Cała płyta została nagrana w Warszawie w Camey Studio. <br />
</div>

